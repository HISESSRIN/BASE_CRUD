package IMCRene;

public class detalle {
int index;
int cantidad;
public detalle() {
	
	
	
	
}



public detalle(int index, int cantidad) {
	
	this.index = index;
	this.cantidad = cantidad;
}



public int getIndex() {
	return index;
}
public void setIndex(int index) {
	this.index = index;
}
public int getCantidad() {
	return cantidad;
}
public void setCantidad(int cantidad) {
	this.cantidad = cantidad;
}




}
